package vo;

public class ChattingData {
	int chattingNum;
	int depNum;
	int userNum;
	String chattingContent;
	public int getChattingNum() {
		return chattingNum;
	}
	public void setChattingNum(int chattingNum) {
		this.chattingNum = chattingNum;
	}
	public int getDepNum() {
		return depNum;
	}
	public void setDepNum(int depNum) {
		this.depNum = depNum;
	}
	public int getUserNum() {
		return userNum;
	}
	public void setUserNum(int userNum) {
		this.userNum = userNum;
	}
	public String getChattingContent() {
		return chattingContent;
	}
	public void setChattingContent(String chattingContent) {
		this.chattingContent = chattingContent;
	}
}
